
import Kelley
using Kelley
Q=[0.016013972 -0.0039697030 -0.001794293 -0.0055048079 -0.001717427;
    -0.003969703 0.0081052450 0.004829187 -0.0005289349 0.002500377;
    -0.001794293 0.0048291866 0.008496151 0.0027656561 0.001679376;
    -0.005504808 -0.0005289349 0.002765656 0.0113728191 0.002526707;
    -0.001717427 0.0025003769 0.001679376 0.0025267072 0.008514379]

r=[0.042362739, -0.004334827,0.006179669,0.020158481,0.010211372]

n=5

g(x)=return[
    x[1:n]'*Q*x[1:n]-0.1,
    sum(x[i] for i in 1:n)-1
]

f(x)=-(sum(r[i]*x[i] for i in 1:n))

optimize!(KelleyAlgorithm(),f,g,n, lb=[0 for i in 1:n], ub=[0.5 for i in 1:n])

println(Kelley.show_objective())